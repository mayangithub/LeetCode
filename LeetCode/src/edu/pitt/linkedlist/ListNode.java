package edu.pitt.linkedlist;

/**
 * Definition for singly-linked list.
 * @author yanma 
 * @version 2015-02-22
 */
public class ListNode {
	/**
	 * Definition for singly-linked list.
	 * public class ListNode {
	 *     int val;
	 *     ListNode next;
	 *     ListNode(int x) {
	 *         val = x;
	 *         next = null;
	 *     }
	 * }
	 */
	public int val;
	public ListNode next;
	public ListNode(int x) {
		val = x;
		next = null;
	}
}
